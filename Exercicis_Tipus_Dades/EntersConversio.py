# Demanar dos nombres amb decimals (float) a l'usuari
num1= float(input("Intrudueix el primer nombre amb decimals:"))
num2= float(input("Intrudueix el segon nombre amb decimals:"))

# Realitar l'operació i que el resultat el passi a numero enet
suma = int(num1+num2)

# Mostrar el resultat
print(f"la suma dels valors {num1} + {num2} és: {suma}")
